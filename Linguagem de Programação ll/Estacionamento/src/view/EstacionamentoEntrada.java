    private void tableSaidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSaidaMouseClicked
        int x = tableSaida.getSelectedRow();
        
        InputModeloS.setText(C.ListaDeCarros.get(x).getModelo());
        inputPlacaS.setText(C.ListaDeCarros.get(x).getPlaca());
    }//GEN-LAST:event_tableSaidaMouseClicked
    
    
        public boolean verificaCadastrado() {
        for(Carro car: C.ListaDeCarros) {
                if (car.getPlaca().equals(inputPlaca.getText())) {
                    JOptionPane.showMessageDialog(null, "Este carro com essa placa está no estacionamento!");
                    return false;
                }
            }
            return true;
        }
        
        public void limpaComponentes() {
            inputModelo.setText("");
            inputPlaca.setText("");
        }
    
        public void loadTableCadastro(){
        Data D = new Data();
        DefaultTableModel modelo = (DefaultTableModel) tableCarros.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) tableSaida.getModel();
        
        
        for(int i = C.ListaDeCarros.size() - 1; i < C.ListaDeCarros.size(); i++) {
            
            modelo.addRow(new Object[]{
                              C.ListaDeCarros.get(i).getPlaca(),
                              C.ListaDeCarros.get(i).getModelo(),
                              D.formataHora(C.ListaDeCarros.get(i).data.getHora()) + ":" +
                              D.formataHora(C.ListaDeCarros.get(i).data.getMinuto()) + ":" +
                              D.formataHora(C.ListaDeCarros.get(i).data.getSegundos()),
                              C.ListaDeCarros.get(i).data.getData()});
            modelo2.addRow(new Object[]{
                              C.ListaDeCarros.get(i).getPlaca(),
                              C.ListaDeCarros.get(i).getModelo()}); 
        }
        //tableCarros.getColumnModel().getColumn(0).setPreferredWidth(200);   REDIMENSIONA TAMANHO DA COLUNA
    }
