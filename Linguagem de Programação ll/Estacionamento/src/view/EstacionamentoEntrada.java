    private void btn_IncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IncluirActionPerformed
        //VERIFICA SE TODOS CAMPOS ESTÃO PREENCHIDOS
        if (inputPlaca.getText().equals("") || inputModelo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor preencher todos os campos!");
        } else {
            
            if(verificaCadastrado() == true) {
                Carro carro = new Carro(inputPlaca.getText(), inputModelo.getText());
                C.addLista(carro);

                for(Carro car: C.ListaDeCarros) {
                    System.out.println(car);
                }
            
                loadTableCadastro();
            }
            
        }
        limpaComponentes();
    }//GEN-LAST:event_btn_IncluirActionPerformed

