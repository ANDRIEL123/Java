    public void verificaCompetenciaCadastrado(){
        for(int i = 0; i < comp.ListaCompetencia.size(); i++){
            if(comp.ListaCompetencia.get(i).getData().equals(FMT_CompetenciaCadastro.getText())){
                cadastrado = true;
                break;
            }else{
                cadastrado = false;
            }
        }
    }
    
    public void ZeraArrayList(){
        for(int i = 0; i < comp.ListaCompetencia.size(); i++){
            comp.ListaCompetencia.remove(i);
        } 
    }
    
    private void BTN_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_CadastrarActionPerformed
        verificaCompetenciaCadastrado();
        if(cadastrado == false){
            ListadeContas L = new ListadeContas();
            L.setVisible(true);
            L.setCompetenciaInicial(FMT_CompetenciaCadastro.getText());
            JOptionPane.showMessageDialog(null,"Iniciando processo de inclusão dos dados da competência...");
        }else{
            JOptionPane.showMessageDialog(null,"Competência já cadastrada, acesse a guia Controle para ter acesso a competências já cadastradas!");
        }

    }//GEN-LAST:event_BTN_CadastrarActionPerformed

