    public void populaJComboBox(javax.swing.JComboBox<String> jComboBox1){
        jComboBox1.removeAllItems();
        for(int i = 0; i < comp.ListaCompetencia.size(); i++){
            jComboBox1.addItem(comp.ListaCompetencia.get(i).getData());
        }
    }

    private void jComboBox1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBox1MouseClicked
        ZeraArrayList();
        try {
            CompDAO.trazDadosDoBancoParaoSistema(comp.ListaCompetencia);
        } catch (SQLException ex) {
            System.err.println("Erro ao trazer os dados " + ex);
        }
        populaJComboBox(jComboBox1);
    }//GEN-LAST:event_jComboBox1MouseClicked

